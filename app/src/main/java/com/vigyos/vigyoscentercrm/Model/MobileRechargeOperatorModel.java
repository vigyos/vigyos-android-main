package com.vigyos.vigyoscentercrm.Model;

public class MobileRechargeOperatorModel {

    private String operatorId;
    private String operatorName;
    private String operatorCategory;
    private String key;
    private String name;

    public MobileRechargeOperatorModel() { }

    public String getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(String operatorId) {
        this.operatorId = operatorId;
    }

    public String getOperatorName() {
        return operatorName;
    }

    public void setOperatorName(String operatorName) {
        this.operatorName = operatorName;
    }

    public String getOperatorCategory() {
        return operatorCategory;
    }

    public void setOperatorCategory(String operatorCategory) {
        this.operatorCategory = operatorCategory;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
